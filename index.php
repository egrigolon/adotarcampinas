        <section class="content about">
            <h1><i class="fa fa-heart"></i> Sobre nossa ONG</h1>
            <p>A Adotar Campinas foi criada por um grupo de pessoas que atuam na causa animal há mais de dez anos na cidade de Campinas - SP. É uma entidade ambientalista não governamental, sem fins lucrativos ou partidários. Busca formas efetivas de combater todo e qualquer tipo de violência contra os animais. É solidária ao sofrimento e necessidades de todos os seres humanos e não humanos. O objetivo da entidade é somar forças com outras entidades afins, para juntas conquistar e preservar os direitos dos animais.</p>
            <aside class="mission-vison-values">
                <h2>Missão</h2>
                <ul>
                    <li>Garantir o bem estar e defender os direitos dos animais;</li>
                    <li>Divulgar a legislação já existente e incentivar a criação de leis que protejam os animais;</li>
                    <li>Mobilizar autoridades na punição de crimes contra os animais;</li>
                    <li>Transformar a realidade dos que se encontram em situações de maus tratos e abandono através de atuações preventivas\educativas junto a população;</li>
                    <li>Propagar a guarda responsável e a esterilização de cães e gatos, assim reduzir o número de animais abandonados;</li>
                    <li>Coibir o abandono seja através da conscientização sobre a Posse Responsável, através de Campanhas de Esterilização dos animais desamparados e/ou domiciliados, através da aplicação da Lei;</li>
                    <li>Sempre que possível, dar atendimento a animais em situações de emergência;</li>
                    <li>Manter parcerias com clinicas e profissionais veterinários garantindo um valor acessível ao atendimento de animais de famílias carentes;</li>
                    <li>Estimular a adoção de animais abandonados através de ações que viabilizem a junção de animais para adoção e adotantes.</li>
                </ul>
                <h2>Visão</h2>
                <p>Ser uma ONG de referência na defesa dos direitos dos animais. Reconhecida pela comunidade como uma entidade comprometida com a melhoria continua na qualidade de vida dos mesmos. Ter como resultado a relação harmoniosa de pessoas/animais.</p>

                <h2>Valores</h2>
                <ul>
                    <li>Todos os seres devem ser protegidos e respeitados de igual modo; o homem não é superior às demais espécies e não deve explorá-las ou subjugá-las;</li>
                    <li>Repudio a violência ou qualquer forma de crueldade aos animais;</li>
                    <li>Trabalho com ética, honestidade, respeito e reconhecimento aos voluntários, mantenedores, associados, comunidade e afins;</li>
                    <li>Responsabilidade Social.</li>
                </ul>
            </aside>
